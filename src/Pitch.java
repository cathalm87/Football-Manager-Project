// Pitch class to control the ball position
public class Pitch {
	
	// Declare string value called ballPosition and initialize as "MID" as the ball will always start in midfield i.e. Kickoff
	String ballPosition = "MID";

	// Getter and setter for ball position 
	public String getBallPosition() {
		return ballPosition;
	}

	public void setBallPosition(String ballPosition) {
		this.ballPosition = ballPosition;
	}
	
	
}
